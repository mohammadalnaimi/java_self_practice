/**
 * this class about the escape sequences
 */

public class CoffeeTea {

    public static void main(String[] args) {
        System.out.println("\"\\__/\"");
        /*
        to print "you need to write \"
        to print \you need to write \\
        to print / you need to write it alone without any sequences
        to print "you need to write \"
        */
        System.out.println("He said \"Hello\"."); // " need to write \"
        System.out.println("Would you like \'coffee\' or 'tea'?");// to print ' you cant write \' or without anything
    }


}

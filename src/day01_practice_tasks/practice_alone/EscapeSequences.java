package practice_alone;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.print("\"");
        System.out.print("\\");

        System.out.print("__/");
        System.out.println("\"");
        System.out.println("He said \"Hello\".");

        System.out.println("Would you like  \'coffee\' or 'tea' ?");

    }
}
